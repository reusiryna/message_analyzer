function getVerdict(userGender, opponentGender) {
    const verdicts = {
        'female_male': {
            winner: 'user',
            reason: 'Ви праві, бо ви жінка. Чоловік за визначенням не може мати рації в суперечці з жінкою. Це аксіома всесвітньої справедливості.'
        },
        'female_female': {
            winner: 'user',
            reason: 'Ви праві, бо та інша дівчина дурна. Жодних додаткових доказів не потрібно. Логіка!'
        },
        'male_female': {
            winner: 'opponent',
            reason: 'Ви не праві, бо чоловік ніколи не буває правий у суперечці з жінкою. Навіть не намагайтеся. Це вже давно доведено наукою (не нашою, але все одно).'
        },
        'male_male': {
            winner: 'nobody',
            reason: getDrawReason()
        }
    };

    const key = `${userGender}_${opponentGender}`;
    return verdicts[key] || verdicts['male_male'];
}

